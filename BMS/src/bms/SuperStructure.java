package bms;

public class SuperStructure {

    public double crack;
    public double powerCrack;
    public double corrosion;
    public double damageRust;
    public double disSeam;
    public double defBeam;
    public double resBeam;
    public double crackBolt;

    public SuperStructure (double a, double b, double c, double d,
                           double e, double f, double g, double h) {
        crack = a;
        powerCrack = b;
        corrosion = c;
        damageRust = d;
        disSeam = e;
        defBeam = f;
        resBeam = g;
        crackBolt = h;
    }
}
